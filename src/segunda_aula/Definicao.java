package segunda_aula;

public class Definicao {

	public static void main(String[] args) {

		// classe - Uma classe é um modelo ou uma planta que define as propriedades
		// (atributos) e comportamentos (métodos) comuns a todos os objetos do mesmo
		// tipo.
		
		// objetos - Um objeto é uma instância de uma classe. Ele representa uma
		// entidade específica que possui estados (valores dos atributos) e
		// comportamentos (métodos).
		
		// Atributos estáticos são variáveis de classe, ou seja, pertencem à classe e
		// não a instâncias específicas da classe.
		
		// Métodos estáticos são funções que pertencem à classe e não a instâncias
		// específicas da classe.
		
		// Encapsulamento = oculta particularidades de um objeto, public package
		// protected e private
		
		// Heranca= recebe comportamentos ou atributos de outra classe
		
		// Polimorfismo = é uma caracteristica que permite que metodos e objetos sejam
		// usados de maneira difente.Ex mesma função com mesmo nome e com parametros
		// diferentes
		
		// Classe Abastrata = Uma classe abstrata é uma classe que não pode ser instanciada diretamente
		
		// Interface =  Uma interface é um contrato que define um conjunto de métodos que uma classe deve implementar. 

	}

}
